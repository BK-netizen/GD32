/*!
* 说明     写数据到OLED模块
* 输入[1]  data:需要写入的数据
* 输入[2]  type:数据类型
* 返回值   无
*/
static Drv_Err bsp_oled_write_byte(uint8_t data, oled_cd_selection type)
{
    uint8_t tx_buf[2];

    if(type){
        tx_buf[0] = 0x40;
    }else{
        tx_buf[0] = 0x00;
    }
    tx_buf[1]=data;
    
    return driver_i2c_poll_write(&OLED_I2C,OLED_ADDR,tx_buf,2);
}

/*!
* 说明     将x-y坐标系转换为页面列坐标系
* 输入[1]  x:x坐标
* 输入[2]  y:y坐标
* 返回值   无
*/
static void bsp_oled_set_pos(uint8_t x, uint8_t y) 
{
    x=x+2;
    /* set page start address for page addressing mode */
    bsp_oled_write_byte(0xb0+y,OLED_CMD);
    /* set higher column start address */
    bsp_oled_write_byte(((x&0xf0)>>4)|0x10,OLED_CMD);
    /* set lower column start address */
    bsp_oled_write_byte((x&0x0f),OLED_CMD);
}

/*!
* 说明     幂计算
* 输入[1]  m:基数
* 输入[2]  n:索引号
* 返回值   计算结果
*/
static uint32_t bsp_oled_pow(uint8_t m,uint8_t n)
{
    uint32_t result = 1;

    while(n--){
        result*=m;
    }

    return result;
}
